#!/usr/bin/env python3
"""
    This module Contains a method add that takes in two floats
    and returns their sum
"""


def add(a: float, b: float) -> float:

    """This is a function that takes in two floats and returns their sum

    Args:
        a: the first number
        b: the second number

    Returns:
        sum of the two numbers

    Raises:
        No error checks
    """

    return (a + b)
