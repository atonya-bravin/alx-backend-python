#!/usr/bin/env python3
"""
    This module contains a method floor that
    takes in a float number and returns its floor
"""

import math


def floor(n: float) -> int:
    """
        This function calculates and returns the floor of a number

        Args:
            n: the floating point number

        Return:
            Returns the floor of the float number

        Raises:
            No error checks
    """

    return (math.floor(n))
