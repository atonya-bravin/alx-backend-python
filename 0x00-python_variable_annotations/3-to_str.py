#!/usr/bin/env python3
"""
    This module contains a method to_str
    that takes in a float and returns ints string rep.
"""


def to_str(n: float) -> str:
    """
        This function takes in a float number
        and returns its string representation.

        Args:
            n: the float number

        Returns:
            A string representation of the float number

        Raises:
            No error checks
    """

    return (str(n))
